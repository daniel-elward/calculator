function add (valueOne, valueTwo) {
    let sum = valueOne + valueTwo

    return sum
}

function subtract (valueOne, valueTwo) {
    let sum = valueOne - valueTwo

    return sum
}

function multiply (valueOne, valueTwo) {
    let sum = valueOne * valueTwo

    return sum
}

function divide (valueOne, valueTwo) {
    let sum = valueOne / valueTwo

    return sum
}

